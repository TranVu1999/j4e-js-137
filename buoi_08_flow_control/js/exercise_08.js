/**
 * Write a program to print multiplication talbe of given number.
 * For example:
 * +) Input: N = 5
 * +) Output:
 * 5 x 1 = 5
 * 5 x 2 = 5
 * 5 x 3 = 5
 * ...
 * 5 x 10 = 10
 *
 */

const N = 5;
for (let i = 1; i <= 10; i++) {
    console.log(N + " x " + i + " = " + N * i);
    // console.log(`${N} x ${i} = ${N * i}`); // Template Literals
}






